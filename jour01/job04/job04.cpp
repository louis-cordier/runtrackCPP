#include <iostream>
using namespace std;

int main() {
    double nombre1, nombre2, somme;

    // Demander à l'utilisateur d'entrer le premier nombre
    cout << "Entrez le premier nombre: ";
    cin >> nombre1;

    // Demander à l'utilisateur d'entrer le deuxième nombre
    cout << "Entrez le deuxième nombre: ";
    cin >> nombre2;

    // Calculer la somme
    somme = nombre1 + nombre2;

    // Afficher le résultat
    cout << "La somme de " << nombre1 << " et " << nombre2 << " est: " << somme << endl;

    return 0;
}
